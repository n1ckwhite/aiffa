import type { ContributionInviteVariant } from '../types';

export const getContributionTexts = (variant: ContributionInviteVariant | undefined) => {
  const mode = variant ?? 'materials';
  const title = mode === 'projects' ? 'Создавайте и улучшайте проекты' : 'Внесите вклад в материалы';
  const description =
    mode === 'projects'
      ? 'Любой участник может сделать проект (предложить и создать новый), дополнить существующий проект (уточнить формулировки, добавить шаги и критерии), а также улучшить проект (исправить неточности, предложить идеи и улучшения). Любой PR к проектам фиксируется и подсвечивается — ваш вклад будет виден сообществу.'
      : 'Любой участник может предложить правку или добавить материал. Каждый пункт задачи, каждое изменение в материалах и любой PR фиксируются и подсвечиваются в проекте — это ваш личный вклад в сообщество. Ваша работа видна другим и будет отмечена ссылкой на ваш GitHub‑профиль.';
  return { title, description };
};


